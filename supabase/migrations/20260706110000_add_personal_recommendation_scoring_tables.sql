drop policy if exists "Users can read own personal recommendation results" on public.personal_recommendation_results;
create policy "Users can read own personal recommendation results"
on public.personal_recommendation_results for select
to authenticated
using (
  exists (
    select 1
    from public.personal_recommendation_runs prr
    where prr.run_id = personal_recommendation_results.run_id
      and prr.user_id = (select public.current_user_id())
  )
);


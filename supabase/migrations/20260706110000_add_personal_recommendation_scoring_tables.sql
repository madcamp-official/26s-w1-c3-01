create policy "Users can create own personal recommendation results"
on public.personal_recommendation_results for insert
to authenticated
with check (
  exists (
    select 1
    from public.personal_recommendation_runs prr
    where prr.run_id = personal_recommendation_results.run_id
      and prr.user_id = (select public.current_user_id())
  )
);

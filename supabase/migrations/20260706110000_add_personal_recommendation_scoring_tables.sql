create table if not exists public.user_preferences (
  user_id bigint primary key references public.users(user_id) on delete cascade,
  budget_min int check (budget_min is null or budget_min >= 0),
  budget_max int check (budget_max is null or budget_max >= 0),
  updated_at timestamptz not null default now(),
  constraint user_preferences_budget_range_check
    check (budget_min is null or budget_max is null or budget_min <= budget_max)
);

create table if not exists public.reviews (
  review_id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(user_id) on delete cascade,
  menu_id bigint not null references public.menus(menu_id) on delete cascade,
  rating int not null check (rating between 1 and 5),
  comment text,
  created_at timestamptz not null default now()
);

create table if not exists public.user_menu_interactions (
  interaction_id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(user_id) on delete cascade,
  menu_id bigint not null references public.menus(menu_id) on delete cascade,
  interaction_type text not null
    check (interaction_type in ('view', 'like', 'pick', 'dislike', 'bookmark')),
  created_at timestamptz not null default now()
);

create table if not exists public.personal_recommendation_runs (
  run_id bigint generated by default as identity primary key,
  user_id bigint not null references public.users(user_id) on delete cascade,
  algorithm_version text not null default 'personal-weighted-v1',
  config_json jsonb,
  generated_at timestamptz not null default now()
);

create table if not exists public.personal_recommendation_results (
  result_id bigint generated by default as identity primary key,
  run_id bigint not null references public.personal_recommendation_runs(run_id) on delete cascade,
  menu_id bigint not null references public.menus(menu_id) on delete cascade,
  rank_no int not null check (rank_no > 0),
  total_score numeric(5, 2) not null check (total_score >= 0 and total_score <= 100),
  scores_json jsonb,
  reason text,
  is_new_suggestion boolean not null default false,
  unique (run_id, rank_no),
  unique (run_id, menu_id)
);

alter table public.personal_recommendation_results
  add column if not exists scores_json jsonb;

alter table public.meal_history
  add column if not exists personal_recommendation_run_id bigint
  references public.personal_recommendation_runs(run_id) on delete set null;

